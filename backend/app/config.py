# Модель риск-скоринга (rule-based, без ML)
#
# score = age_score + kpd_gap_score + wear_score + defect_score   (0-100)
#
#   age_score    -- чем старше объект (год ввода), тем выше риск
#   kpd_gap_score-- разница КПД проектного и фактического (деградация эффективности)
#   wear_score   -- процент износа из датасета (когда он указан)
#   defect_score -- бинарный флаг "зафиксирован дефект" = исходное
#                   техническое состояние "не удов." в датасете
